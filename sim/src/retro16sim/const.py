type WORD = int
type Addr = int

WORD_BITS = 16
WORD_MASK = 0xFFFF
ADDR_MASK = 0xFFFF

BYTE_BITS = 8
BYTE_MASK = 0x00FF

NEGATIVE_BIT = 0x8000  # bit 15
IMM6_MASK = 0x003F  # 6 bits
IMM6_SIGNBIT = 0x0020  # sign bit (bit 5)

OFF12_MASK = 0x0FFF
OFF12_SIGNBIT = 0x0800

OPCODE_SHIFT = 12
OPCODE_BITS = 4
OPCODE_MASK = (1 << OPCODE_BITS) - 1  # 0x000F

REG_BITS = 3
REG_MASK = (1 << REG_BITS) - 1  # 0x0007
REG_SHIFT_RS2 = 3
REG_SHIFT_RS1 = 6
REG_SHIFT_RD = 9

R0 = 0
R1 = 1
R2 = 2
R3 = 3
R4 = 4
R5 = 5
R6 = 6
SP = 7

MEM_SIZE = 0x10000

ROM_START = 0x0000
ROM_END = 0x3FFF

VRAM_START = 0x8000
VRAM_END = 0xBFFF

PPU_REG_BASE = 0xC000
APU_REG_BASE = 0xC100
IO_REG_BASE = 0xC200
